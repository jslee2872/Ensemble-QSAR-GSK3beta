def analyze_structure_activity(smiles_list, activities, descriptors=None):
    """Analyze molecular structure-activity relationships"""

    # 1. Calculating Structure Properties
    if descriptors is None:
        descriptors = {}
        for idx, smiles in enumerate(smiles_list):
            mol = Chem.MolFromSmiles(smiles)
            if mol:
                descriptors[idx] = {
                    'logP': Descriptors.MolLogP(mol),
                    'TPSA': Descriptors.TPSA(mol),
                    'HBD': Descriptors.NumHDonors(mol),
                    'HBA': Descriptors.NumHAcceptors(mol),
                    'MW': Descriptors.MolWt(mol),
                    'RotBonds': Descriptors.NumRotatableBonds(mol),
                    'ArRings': Descriptors.NumAromaticRings(mol),
                    'HetArRings': rdMolDescriptors.CalcNumAromaticHeterocycles(mol)
                }

    # 2. Correlation analysis
    descriptor_names = list(next(iter(descriptors.values())).keys())
    correlation_data = []

    for desc_name in descriptor_names:
        desc_values = [descriptors[idx][desc_name] for idx in range(len(smiles_list))
                      if idx in descriptors]
        activity_values = [activities[idx] for idx in range(len(smiles_list))
                          if idx in descriptors]

        if len(desc_values) > 1:
            pearson_r, pearson_p = stats.pearsonr(desc_values, activity_values)
            spearman_r, spearman_p = stats.spearmanr(desc_values, activity_values)

            correlation_data.append({
                'Descriptor': desc_name,
                'Pearson_r': pearson_r,
                'Pearson_p': pearson_p,
                'Spearman_r': spearman_r,
                'Spearman_p': spearman_p,
                'Significant': pearson_p < 0.05 or spearman_p < 0.05
            })

    # 3. Output the results
    correlation_df = pd.DataFrame(correlation_data)
    significant_correlations = correlation_df[correlation_df['Significant']]

    print("\nSignificant Structure-Activity Correlations:")
    if len(significant_correlations) > 0:
        for _, row in significant_correlations.sort_values('Pearson_r',
                                                         key=abs,
                                                         ascending=False).iterrows():
            print(f" • {row['Descriptor']}: Pearson r = {row['Pearson_r']:.4f}, "
                 f"p = {row['Pearson_p']:.4f} | Spearman r = {row['Spearman_r']:.4f}, "
                 f"p = {row['Spearman_p']:.4f}")
    else:
        print(" No significant correlations found")

    return correlation_df

