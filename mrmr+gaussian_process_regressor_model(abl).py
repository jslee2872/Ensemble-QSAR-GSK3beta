def optimize_feature_dimension_reduction(X, y, feature_groups, cv=5, n_trials=30):
    """
    Finding the optimal variance ratio for PCA dimensionality reduction
    """
    print("Optimizing feature dimensionality reduction...")

    def objective(trial):
        # Optimize the PCA variance ratio for each group
        pca_variance_ratios = {}
        for group in feature_groups.keys():
            pca_variance_ratios[group] = trial.suggest_float(f'{group}_pca_ratio', 0.7, 0.99)

        # Evaluate performance with cross-validation
        cv_folds = KFold(n_splits=cv, shuffle=True, random_state=42)
        cv_scores = []

        for train_idx, val_idx in cv_folds.split(X):
            X_train, X_val = X[train_idx], X[val_idx]
            y_train, y_val = y[train_idx], y[val_idx]

            # Apply PCA by feature groups
            X_train_reduced, X_val_reduced, pca_components = apply_group_pca(
                X_train, X_val, feature_groups, pca_variance_ratios
            )

            # Model training and performance evaluation
            model = xgb.XGBRegressor(n_estimators=100, random_state=42)
            model.fit(X_train_reduced, y_train)
            preds = model.predict(X_val_reduced)
            score = r2_score(y_val, preds)
            cv_scores.append(score)

        # About dimensionality reduction
        total_components = sum(pca_components.values())
        orig_dims = sum(feature_groups.values())
        reduction_ratio = 1 - (total_components / orig_dims)

        # Weighted average of average R² score and dimensionality reduction
        mean_score = np.mean(cv_scores)
        combined_score = mean_score * 0.8 + reduction_ratio * 0.2

        print(f"Trial {trial.number}: R² = {mean_score:.4f}, dimensionality reduction ratio = {reduction_ratio:.2f}, total number of features: {total_components}")

        return combined_score

    # Create Optuna studies and run optimizations
    study = optuna.create_study(direction='maximize')
    study.optimize(objective, n_trials=n_trials)

    # Optimal parameters
    best_params = study.best_params
    best_pca_ratios = {group: best_params[f'{group}_pca_ratio']
                       for group in feature_groups.keys()}

    print("\nOptimal PCA Variance Ratio:")
    for group, ratio in best_pca_ratios.items():
        print(f"- {group}: {ratio:.4f}")

    return best_pca_ratios

def apply_group_pca(X_train, X_val, feature_groups, pca_variance_ratios):
    """
    Apply PCA by feature groups
    """
    start_idx = 0
    X_train_reduced_parts = []
    X_val_reduced_parts = []
    pca_components = {}

    for group, count in feature_groups.items():
        end_idx = start_idx + count

        # Extract group data
        X_train_group = X_train[:, start_idx:end_idx]
        X_val_group = X_val[:, start_idx:end_idx]

        # Minimum Explained Variance
        variance_ratio = pca_variance_ratios[group]

        if X_train_group.shape[1] > 1:  # Apply PCA only if you have more than one feature
            # Apply PCA
            pca = PCA(n_components=variance_ratio, random_state=42)
            X_train_group_reduced = pca.fit_transform(X_train_group)
            X_val_group_reduced = pca.transform(X_val_group)

            pca_components[group] = X_train_group_reduced.shape[1]
        else:
            # Keep as is if you have 1 feature
            X_train_group_reduced = X_train_group
            X_val_group_reduced = X_val_group
            pca_components[group] = 1

        X_train_reduced_parts.append(X_train_group_reduced)
        X_val_reduced_parts.append(X_val_group_reduced)

        start_idx = end_idx

    # Combine dimensionally reduced data
    X_train_reduced = np.hstack(X_train_reduced_parts)
    X_val_reduced = np.hstack(X_val_reduced_parts)

    return X_train_reduced, X_val_reduced, pca_components

