def optimize_feature_selection(X, y, feature_groups, cv=5, n_trials=50, timeout=3600):
    """
    A function that uses Bayesian optimization to find optimal feature selection parameters.
    """
    print("Finding feature selection parameters with Bayesian optimization...")

    # Set a percentage range for each feature group
    groups = list(feature_groups.keys())

    def objective(trial):
        # Optimize total number of features (100-1000)
        total_features = trial.suggest_int('total_features', 100, 1000, step=50)

        # Optimize target percentages by group (sum to 1)
        ratios = []
        for i, group in enumerate(groups[:-1]):
            # The last group is calculated as the remaining percentage
            ratio_name = f'{group}_ratio'
            if i == 0:
                ratio = trial.suggest_float(ratio_name, 0.1, 0.5)
            else:
                remaining = 1.0 - sum(ratios)
                ratio = trial.suggest_float(ratio_name, 0.05, min(0.5, remaining))
            ratios.append(ratio)

        # The last group is set to sum to 1
        last_ratio = 1.0 - sum(ratios)
        ratios.append(last_ratio)

        # Create a target percentage dictionary
        target_ratios = {group: ratio for group, ratio in zip(groups, ratios)}

        # Optimize domain weights
        domain_weights = {}
        for group in groups:
            weight_name = f'{group}_weight'
            domain_weights[group] = trial.suggest_float(weight_name, 0.5, 3.0)

        # Optimize correlation filtering thresholds
        corr_threshold = trial.suggest_float('corr_threshold', 0.7, 0.98)

        # Evaluate performance with cross-validation
