def objective_group_importance_selection(X_train_scaled, y_train, new_feature_groups):
    """
    Use the Minimum Redundancy Maximum Relevance (mRMR) algorithm to select the
 optimal feature selection function
    """
    print("\nPerforming selection with objective mRMR-based feature importance assessment...")

    from sklearn.feature_selection import mutual_info_regression

    # Set the number of features and target number of features
    n_features = X_train_scaled.shape[1]
    min_features = 200  # You need at least this many features

    # 1. Calculate the amount of mutual information (relevance) for targets across all features
    print("  • Calculating feature-target mutual information...")
    relevance = mutual_info_regression(X_train_scaled, y_train, random_state=42)
    relevance = np.nan_to_num(relevance)

    # 2. Set a minimum number of features for each group
    min_group_features = {
        'ECFP': max(50, int(0.1 * new_feature_groups.get('ECFP', 0))),  # Minimum of 50 or 10% of
        '2D': max(10, int(0.2 * new_feature_groups.get('2D', 0))),      # 10 or 20% minimum
        'GSK3': max(15, int(0.3 * new_feature_groups.get('GSK3', 0))),  # Minimum of 15 or 30% of
        '3D': max(10, int(0.5 * new_feature_groups.get('3D', 0))),      # Minimum of 10 or 50% of
        'Mordred': max(30, int(0.1 * new_feature_groups.get('Mordred', 0)))  # Minimum of 30 or 10% of
    }

    # 3. Minimally relevant feature selection from each group
    selected_features = []
    start_idx = 0

    print("  • Selecting minimum feature selection by group...")
    for group, count in new_feature_groups.items():
        end_idx = start_idx + count
        group_relevance = relevance[start_idx:end_idx]

        # Minimum number of features to select in this group
        n_select = min(min_group_features.get(group, 5), count)

        # Most relevant feature selection
        if n_select > 0 and len(group_relevance) > 0:
            top_indices = np.argsort(group_relevance)[-n_select:]
            group_selected = [idx + start_idx for idx in top_indices]
            selected_features.extend(group_selected)
            print(f"  • Group {group}: {n_select} initial selection of features.")

        start_idx = end_idx

    # 4. Remaining feature selection with the mRMR algorithm
    remaining = min_features - len(selected_features)

    if remaining > 0:
        print(f"  • Continuing {remaining} feature selection with mRMR...")

        # Feature candidates not yet selected
        candidates = np.array([i for i in range(n_features) if i not in selected_features])

        # 5. Perform mRMR iterations
        for i in range(remaining):
            if len(candidates) == 0:
                break

            # Calculate the mRMR score for each candidate feature
            mrmr_scores = np.zeros(len(candidates))

            for j, candidate in enumerate(candidates):
                # Relevance terms
                relevance_term = relevance[candidate]

                # Redundancy term (mutual redundancy with already selected features)
                if len(selected_features) > 0:
                    redundancy_values = []

                    # Use only up to 100 sampled features for computational efficiency
                    sample_selected = selected_features
                    if len(selected_features) > 100:
                        sample_selected = np.random.choice(selected_features, 100, replace=False)

                    # The amount of mutual information between candidate features and already selected features.
                    x_candidate = X_train_scaled[:, candidate].reshape(-1, 1)

                    for selected in sample_selected:
                        x_selected = X_train_scaled[:, selected].reshape(-1, 1)
                        mi = mutual_info_regression(x_candidate, X_train_scaled[:, selected])[0]
                        redundancy_values.append(mi)

                    redundancy = np.mean(redundancy_values) if redundancy_values else 0
                else:
                    redundancy = 0

                # mRMR Score = Relevance - Redundancy
                mrmr_scores[j] = relevance_term - redundancy

            # Feature selection with the highest mRMR score
            if len(mrmr_scores) > 0:
                best_idx = np.argmax(mrmr_scores)
                selected_features.append(candidates[best_idx])
                candidates = np.delete(candidates, best_idx)

                # Progress output (in 10% increments)
                if (i+1) % (remaining//10 or 1) == 0:
                    print(f"  • Progress mRMR: {i+1}/{remaining} feature selection complete")

    # Returns the final list of selected features
    selected_features = np.array(selected_features)

    # Calculate the distribution of selected features by groups
    feature_distribution = {group: 0 for group in new_feature_groups}
    start_idx = 0
    for group, count in new_feature_groups.items():
        end_idx = start_idx + count
        group_range = range(start_idx, end_idx)

        # Count the number of selected features that belong to each group
        feature_distribution[group] = sum(1 for idx in selected_features if idx in group_range)
        start_idx = end_idx

    # Output the results
    print("\nDistribution of features selected by mRMR by groups:")
    for group, count in feature_distribution.items():
        ratio = count / len(selected_features) * 100
        print(f"  • {group}: {count} features ({ratio:.1f}%)")

    # Generate an importance score as a result of mRMR (1 for selected features, 0 for the rest)
    importance_threshold = 0  # dummy value to simply format the return

    return selected_features, feature_distribution, importance_threshold

