class AdaptiveWeightEnsemble:
    """Adaptive ensembles with performance-based weighting"""
    def __init__(self, base_models=None, meta_model=None):
        # Default Model Settings - Remove LightGBM
        self.base_models = base_models or [
            RandomForestRegressor(n_estimators=200, max_depth=8, min_samples_split=10, min_samples_leaf=5, random_state=42),
            xgb.XGBRegressor(n_estimators=200, learning_rate=0.02, max_depth=6, subsample=0.7, colsample_bytree=0.7, random_state=42),
            # Remove the LightGBM model
            SVR(kernel='rbf', C=5.0, gamma='scale', epsilon=0.1),
            KNeighborsRegressor(n_neighbors=7, weights='distance'),
            ElasticNet(alpha=0.05, l1_ratio=0.7, max_iter=10000)
        ]

        # Meta model settings
        if meta_model is not None:
            self.meta_model = meta_model
        else:
            self.meta_model = None

        # Internal variables
        self.model_weights = None
        self.nn_model = None
        self.nn_weight = 0.0
        self.is_fitted = False
        self.y_scaler = None

    def fit(self, X_train, y_train, X_val, y_val):
        """Ensemble learning with adaptive weights"""
        # Save target scaling information
        y_mean, y_std = np.mean(y_train), np.std(y_train)
        self.y_scaler = {'mean': y_mean, 'std': y_std}
        y_train_scaled = (y_train - y_mean) / y_std

        n_models = len(self.base_models)
        model_preds = np.zeros((X_val.shape[0], n_models))
        model_scores = []

        # Individual model training and performance evaluation
        for i, model in enumerate(self.base_models):
            # Model training
            try:
                if hasattr(model, 'fit') and 'sample_weight' in model.fit.__code__.co_varnames:
                    # Calculate sample weights (higher weights for targets with larger absolute values)
                    weights = 1.0 + 0.2 * np.abs(y_train)
                    model.fit(X_train, y_train, sample_weight=weights)
                else:
                    model.fit(X_train, y_train)

                # Validation set prediction
                preds = model.predict(X_val)
                model_preds[:, i] = preds

                # Calculate performance
                score = r2_score(y_val, preds)
                model_scores.append(max(0, score))  # Prevent negative scores

                print(f"  • model {i+1} ({model.__class__.__name__}) - validation R²: {score:.4f}")
            except Exception as e:
                print(f"  • model {i+1} ({model.__class__.__name__}) - error: {e}")
                model_scores.append(0)
                model_preds[:, i] = np.zeros(X_val.shape[0])

        # Meta-model training (stacking ensembles)
        try:
            # Initializing the meta model
            if self.meta_model is None:
                self.meta_model = GradientBoostingRegressor(n_estimators=80, learning_rate=0.04,
                                                        max_depth=2, subsample=0.7, random_state=42)

            self.meta_model.fit(model_preds, y_val)
            meta_preds = self.meta_model.predict(model_preds)
            meta_score = r2_score(y_val, meta_preds)
            print(f"  • Meta Model - Validation R²: {meta_score:.4f}")
        except Exception as e:
            print(f"  • Meta-model training errors: {e}")

        # Processing neural network models
        if self.nn_model is not None:
            # Neural Network Prediction
            self.nn_model.eval()
            try:
                with torch.no_grad():
                    X_val_tensor = torch.FloatTensor(X_val).to(device)
                    nn_preds = self.nn_model(X_val_tensor).cpu().numpy().flatten()

                # Neural network performance
                nn_score = r2_score(y_val, nn_preds)
                print(f"  • Neural Network Models - Validation R²: {nn_score:.4f}")

                # Determine weights (performance-based)
                if nn_score > 0:
                    if nn_score > np.max(model_scores) + 0.05:
                        # If the neural network is the best performing, it will have high weights
                        self.nn_weight = 0.7
                    elif nn_score > np.max(model_scores):
                        # If the neural network performs moderately well, you can choose medium-high weights
                        self.nn_weight = 0.6
                    elif nn_score > np.mean(model_scores) + 0.05:
                        # Medium weight if significantly better than average
                        self.nn_weight = 0.5
                    elif nn_score > np.mean(model_scores):
                        # Low-medium weight if better than average
                        self.nn_weight = 0.4
                    else:
                        # Lower weighting if below average but positive
                        self.nn_weight = 0.3
                else:
                    # Excludes negative performance
                    self.nn_weight = 0.0

                print(f"  • Neural network weights: {self.nn_weight:.2f}")
            except Exception as e:
                print(f"  • Neural network prediction error: {e}")
                self.nn_weight = 0.0

        # Calculate model weights (you can choose to use Bayesian optimization)
        try:
            self.model_weights = self.optimal_ensemble_weights(model_preds, y_val, method='bayesian')
            print(f"  • Optimized model weights: {[f'{w:.4f}' for w in self.model_weights]}")
        except Exception as e:
            print(f"  • Weight optimization error ({e}), using performance proportional weights")

            # Performance Proportional Weighting
            total_score = sum(model_scores)
            if total_score > 0:
                self.model_weights = [score/total_score for score in model_scores]
            else:
                # If all models are underperforming, use the Equally Weighted
                self.model_weights = [1.0/n_models] * n_models
                print("  • Use Equal Weights (all models underperform)")

        self.is_fitted = True
        return self

    def optimal_ensemble_weights(self, model_preds, y_true, method='bayesian'):
        """Bayesian optimization to find optimal weights"""
        if method == 'bayesian':
            # Objective Function (Maximize R²)
            def objective(weights):
                # Weight normalization
                weights = np.array(weights)
                weights = weights / np.sum(weights)

                # Weighted average forecast
                ensemble_pred = np.sum(model_preds * weights, axis=1)

                # Negative R² (converts to minimization problem)
                return -r2_score(y_true, ensemble_pred)

            # Constraints: sum of weights = 1, all weights >= 0
            constraints = ({'type': 'eq', 'fun': lambda w: np.sum(w) - 1})
            bounds = [(0, 1) for _ in range(model_preds.shape[1])]

            # Initial weights (even)
            initial_weights = np.ones(model_preds.shape[1]) / model_preds.shape[1]

            # Optimize
            result = minimize(
                objective,
                initial_weights,
                method='SLSQP',
                bounds=bounds,
                constraints=constraints
            )

            # Optimal weights
            optimal_weights = result.x
            optimal_weights = optimal_weights / np.sum(optimal_weights)

            return optimal_weights

        elif method == 'grid_search':
            # Simple grid navigation (for 3 or fewer models)
            if model_preds.shape[1] <= 3:
                best_score = -np.inf
                best_weights = None

                # Explore weights in increments of 0.1
                steps = 11  # 0.0, 0.1, ..., 1.0

                if model_preds.shape[1] == 2:
                    for w1 in range(steps):
                        w1 = w1 / (steps - 1)  # 0.0 ~ 1.0
                        w2 = 1.0 - w1

                        weights = np.array([w1, w2])
                        ensemble_pred = np.sum(model_preds * weights, axis=1)
                        score = r2_score(y_true, ensemble_pred)

                        if score > best_score:
                            best_score = score
                            best_weights = weights

                elif model_preds.shape[1] == 3:
                    for w1 in range(steps):
                        w1 = w1 / (steps - 1)
                        for w2 in range(steps):
                            w2 = w2 / (steps - 1)

                            if w1 + w2 > 1.0:
                                continue

                            w3 = 1.0 - w1 - w2

                            weights = np.array([w1, w2, w3])
                            ensemble_pred = np.sum(model_preds * weights, axis=1)
                            score = r2_score(y_true, ensemble_pred)

                            if score > best_score:
                                best_score = score
                                best_weights = weights

                return best_weights
            else:
                # Replace with Bayesian method if more than 3 models
                return self.optimal_ensemble_weights(model_preds, y_true, method='bayesian')

        else:  # 'simple'
            # Simple performance proportional weighting
            scores = []
            for i in range(model_preds.shape[1]):
                score = r2_score(y_true, model_preds[:, i])
                scores.append(max(0, score))  # Prevent negative scores

            total_score = sum(scores)
            if total_score > 0:
                weights = [score/total_score for score in scores]
            else:
                weights = [1.0/model_preds.shape[1]] * model_preds.shape[1]

            return weights

    def predict(self, X):
        """Ensemble forecasting"""
        if not self.is_fitted:
            raise ValueError("Model must be fitted before prediction")

        # Base model predictions
        n_models = len(self.base_models)
        n_samples = X.shape[0]
        model_preds = np.zeros((n_samples, n_models))

        for i, model in enumerate(self.base_models):
            try:
                model_preds[:, i] = model.predict(X)

                # Checking for NaN or infinite values
                if np.any(np.isnan(model_preds[:, i])) or np.any(np.isinf(model_preds[:, i])):
                    print(f"Model {i} prediction contains NaN/Inf values. Replace with average value")
                    model_preds[:, i] = np.nan_to_num(model_preds[:, i], nan=np.nanmean(model_preds[:, i]))
            except Exception as e:
                print(f"Model {i} prediction error: {e}")
                # Replace with the average of other model predictions
                valid_preds = [model_preds[:, j] for j in range(i) if not np.any(np.isnan(model_preds[:, j]))]
                if valid_preds:
                    model_preds[:, i] = np.mean(valid_preds, axis=0)
                else:
                    model_preds[:, i] = self.y_scaler['mean'] if self.y_scaler else 0

        # Meta-model prediction (stacking)
        try:
            stacking_pred = self.meta_model.predict(model_preds)
        except Exception as e:
            print(f"Meta-model prediction error: {e}. Using weighted averages")
            # Replace with weighted average
            stacking_pred = np.sum(model_preds * np.array(self.model_weights), axis=1)

        # Integrate neural network predictions
        if self.nn_model is not None and self.nn_weight > 0:
            try:
                # Neural Network Prediction
                self.nn_model.eval()
                with torch.no_grad():
                    X_tensor = torch.FloatTensor(X).to(device)
                    nn_pred = self.nn_model(X_tensor).cpu().numpy().flatten()

                # Outlier handling
                if np.any(np.isnan(nn_pred)) or np.any(np.isinf(nn_pred)):
                    print("Including NaN/Inf values in neural network predictions. Apply calibration")
                    nn_pred = np.nan_to_num(nn_pred, nan=self.y_scaler['mean'] if self.y_scaler else 0)

                # Calculate weighted averages
                final_pred = (1 - self.nn_weight) * stacking_pred + self.nn_weight * nn_pred
            except Exception as e:
                print(f"Neural Network Prediction Convergence Error: {e}")
                final_pred = stacking_pred
        else:
            final_pred = stacking_pred

        # Limit the range of predicted values (reasonable pChEMBL range)
        final_pred = np.clip(final_pred, 4.0, 10.0)

        return final_pred

