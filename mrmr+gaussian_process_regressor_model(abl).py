        for train_idx, val_idx in cv_folds.split(X):
            X_train, X_val = X[train_idx], X[val_idx]
            y_train, y_val = y[train_idx], y[val_idx]

            # Using scikit-learn's SelectKBest instead of ImprovedFeatureSelector here
            selected_features = []
            remaining_features = total_features
            start_idx = 0

            # Feature selection for each group
            for group, count in feature_groups.items():
                end_idx = start_idx + count
                if end_idx > X.shape[1]:
                    end_idx = X.shape[1]

                # Extract data by group
                X_group_train = X_train[:, start_idx:end_idx]

                # Minimum quota for each group (minimum of 5) + proportional quota
                min_per_group = 5
                proportional_allocation = int((total_features - min_per_group * len(feature_groups)) * target_ratios[group])
                n_select = min(min_per_group + proportional_allocation, end_idx - start_idx)  # 그룹 크기보다 클 수 없음

                # Logging group information
                print(f"  • group {group}: Selection goal {n_select} features (guarantee at least {min_per_group})")

