def validated_feature_selection(X, y, feature_groups, cv_outer=5, cv_inner=3):
    """Reliable feature selection with double cross-validation"""

    # External CV folds (for performance evaluations)
    outer_cv = KFold(n_splits=cv_outer, shuffle=True, random_state=42)

    # Save the results
    outer_scores = []
    selected_features_sets = []

    for train_idx, test_idx in outer_cv.split(X):
        X_train, X_test = X[train_idx], X[test_idx]
        y_train, y_test = y[train_idx], y[test_idx]

        # Internal CV (for optimizing feature selection)
        feature_counts = [100, 200, 300, 400, 500, 700]
        best_score = -np.inf
        best_count = None
        best_selector = None

        for count in feature_counts:
            # Evaluate the number of features with internal cross-validation
            inner_cv = KFold(n_splits=cv_inner, shuffle=True, random_state=42)
            inner_scores = []

            for inner_train_idx, inner_val_idx in inner_cv.split(X_train):
                X_inner_train = X_train[inner_train_idx]
                X_inner_val = X_train[inner_val_idx]
                y_inner_train = y_train[inner_train_idx]
                y_inner_val = y_train[inner_val_idx]

                # Feature selection
                selector = ImprovedFeatureSelector(
                    feature_groups=feature_groups,
                    total_features=count,
                    target_ratios={'ECFP': 0.25, '2D': 0.2, 'GSK3': 0.25, '3D': 0.15, 'Mordred': 0.15},
                    domain_weights={'ECFP': 1.0, '2D': 1.3, 'GSK3': 1.8, '3D': 1.6, 'Mordred': 1.1}
                )

                # Apply feature selections
                X_inner_train_sel = selector.fit_transform(X_inner_train, y_inner_train)
                X_inner_val_sel = selector.transform(X_inner_val)

                # Evaluate performance
                eval_model = xgb.XGBRegressor(n_estimators=100, random_state=42)
                eval_model.fit(X_inner_train_sel, y_inner_train)
                y_pred = eval_model.predict(X_inner_val_sel)
                score = r2_score(y_inner_val, y_pred)
                inner_scores.append(score)

            # Average internal CV score
            mean_score = np.mean(inner_scores)
            print(f"  • internal CV - {count} feature: R² = {mean_score:.4f}")

            if mean_score > best_score:
                best_score = mean_score
                best_count = count

        # Apply feature selection to the entire training set with the optimal number of features
        print(f"  • Number of features selected: {best_count} (R² = {best_score:.4f})")

        final_selector = ImprovedFeatureSelector(
            feature_groups=feature_groups,
            total_features=best_count,
            target_ratios={'ECFP': 0.25, '2D': 0.2, 'GSK3': 0.25, '3D': 0.15, 'Mordred': 0.15},
            domain_weights={'ECFP': 1.0, '2D': 1.3, 'GSK3': 1.8, '3D': 1.6, 'Mordred': 1.1}
        )

        X_train_sel = final_selector.fit_transform(X_train, y_train)
        X_test_sel = final_selector.transform(X_test)

        # Evaluate with external test sets
        final_model = xgb.XGBRegressor(n_estimators=200, random_state=42)
        final_model.fit(X_train_sel, y_train)
        y_pred = final_model.predict(X_test_sel)
        test_score = r2_score(y_test, y_pred)
        outer_scores.append(test_score)
        selected_features_sets.append(final_selector.selected_indices_)

        print(f"  • External CV Fold - Test R²: {test_score:.4f}")

    # Result
    mean_score = np.mean(outer_scores)
    print(f"Final Validated Feature Selection - Average R²: {mean_score:.4f} ± {np.std(outer_scores):.4f}")

    # Find the most frequently selected features
    feature_counts = np.zeros(X.shape[1])
    for selected in selected_features_sets:
        feature_counts[selected] += 1

    # Keep only feature selections from more than 50% of folds
    stable_features = np.where(feature_counts >= cv_outer/2)[0]

    return {
        'stable_features': stable_features,
        'mean_cv_score': mean_score,
        'cv_scores': outer_scores,
        'selected_features_sets': selected_features_sets
    }

class ImprovedFeatureSelector:
    """
    Improved feature selection classes - with multiple importance measures and strict group assignment
    """
    def __init__(self, feature_groups, total_features=400,
                 target_ratios={'ECFP': 0.25, '2D': 0.2, 'GSK3': 0.25, '3D': 0.15, 'Mordred': 0.15},
                 domain_weights={'ECFP': 1.0, '2D': 1.3, 'GSK3': 1.8, '3D': 1.6, 'Mordred': 1.1},
                 corr_threshold=0.9):
        self.feature_groups = feature_groups  # About feature groups
        self.total_features = total_features  # Total number of features to select
        self.target_ratios = target_ratios    # Percentage of goals by group
        self.domain_weights = domain_weights  # Domain weights
        self.corr_threshold = corr_threshold  # Correlation filtering thresholds
        self.selected_indices_ = None
        self.importance_scores_ = None

    def calculate_multiple_importance(self, X, y, group_name):
        """Calculate feature importance in multiple ways"""
        importance_scores = np.zeros(X.shape[1])

        # 1. Lasso Importance
        try:
            lasso = Lasso(alpha=0.01, max_iter=10000, random_state=42)
            lasso.fit(X, y)
            lasso_importance = np.abs(lasso.coef_)
            # Normalization
            if np.sum(lasso_importance) > 0:
                lasso_importance = lasso_importance / np.sum(lasso_importance)
            importance_scores += lasso_importance
        except:
            pass

        # 2. Random Forest Importance
        try:
            rf = RandomForestRegressor(n_estimators=100, max_depth=5, random_state=42)
            rf.fit(X, y)
            rf_importance = rf.feature_importances_
            importance_scores += rf_importance
        except:
            pass

        # 3. Amount of mutual information (Mutual Information)
        try:
            mi_importance = mutual_info_regression(X, y, random_state=42)
            # Normalization
            if np.sum(mi_importance) > 0:
                mi_importance = mi_importance / np.sum(mi_importance)
            importance_scores += mi_importance
        except:
            pass

        # Apply domain weighting
        importance_scores *= self.domain_weights.get(group_name, 1.0)

        return importance_scores

    def correlation_filter(self, X, importance_scores, threshold=0.9):
        """Filtering highly correlated duplicate features"""
        # Calculate the correlation coefficient matrix
        corr_matrix = np.corrcoef(X.T)

        # Find pairs of highly correlated features
        n_features = X.shape[1]
        to_remove = set()

        for i in range(n_features):
            if i in to_remove:
                continue

            for j in range(i+1, n_features):
                if j in to_remove:
                    continue

                # If the correlation coefficient crosses the threshold, the
                if np.abs(corr_matrix[i, j]) > threshold:
                    # Remove less important features
                    if importance_scores[i] < importance_scores[j]:
                        to_remove.add(i)
                    else:
                        to_remove.add(j)

        # Feature indexes not to remove
        keep_indices = [i for i in range(n_features) if i not in to_remove]

        return keep_indices

    def fit(self, X, y):
        """Perform feature selections"""
        # Calculate indexes by group
        self.group_indices_ = {}
        start_idx = 0

        for group_name, feature_count in self.feature_groups.items():
            end_idx = start_idx + feature_count
            self.group_indices_[group_name] = list(range(start_idx, end_idx))
            start_idx = end_idx

        # Calculate the number of feature assignments for each group
        self.group_allocations_ = {}
        remaining_features = self.total_features
        remaining_ratio = 1.0

        # Step 1: Minimum assignments (minimum of 5 features for each group)
        min_per_group = 5
        for group in self.target_ratios:
            if group in self.group_indices_:
                self.group_allocations_[group] = min_per_group
                remaining_features -= min_per_group
                remaining_ratio -= self.target_ratios[group]

        # Step 2: Additional allocations based on percentages
        for group, ratio in self.target_ratios.items():
            if group in self.group_indices_:
                # Allocation by percentage (after minimum allocation)
                adjusted_ratio = ratio / remaining_ratio if remaining_ratio > 0 else 0
                additional = int(remaining_features * adjusted_ratio)
                self.group_allocations_[group] += additional

                # To avoid being allocated more than the group size
                available_features = len(self.group_indices_[group])
                self.group_allocations_[group] = min(self.group_allocations_[group], available_features)

        # The full array of importance scores and selected feature indices
        self.importance_scores_ = np.zeros(X.shape[1])
        selected_indices = []

        # Feature selection for each group
        for group_name, indices in self.group_indices_.items():
            if len(indices) == 0 or group_name not in self.group_allocations_:
                continue

            # Extract data by group
            X_group = X[:, indices]

            # Calculate importance with multiple methods
            group_importance = self.calculate_multiple_importance(X_group, y, group_name)

            # Assign to the entire importance array
            self.importance_scores_[indices] = group_importance

            # Correlation filtering
            if X_group.shape[1] > 1:
                keep_indices = self.correlation_filter(X_group, group_importance, self.corr_threshold)
                filtered_indices = [indices[i] for i in keep_indices]
                filtered_importance = group_importance[keep_indices]
            else:
                filtered_indices = indices
                filtered_importance = group_importance

            # Allocated number of feature selections
            n_to_select = min(self.group_allocations_[group_name], len(filtered_indices))

            if n_to_select > 0 and len(filtered_importance) > 0:
                top_k = np.argsort(filtered_importance)[-n_to_select:]
                group_selected = [filtered_indices[i] for i in top_k]
                selected_indices.extend(group_selected)

        # If there is a difference between the number allocated and the number selected, fill with features of higher importance
        if len(selected_indices) < self.total_features:
            remaining = self.total_features - len(selected_indices)
            unselected = [i for i in range(X.shape[1]) if i not in selected_indices]

            if len(unselected) > 0:
                unselected_importance = self.importance_scores_[unselected]
                additional_indices = np.argsort(unselected_importance)[-remaining:]
                additional_features = [unselected[i] for i in additional_indices]
                selected_indices.extend(additional_features)

        self.selected_indices_ = np.array(selected_indices)
        return self

    def transform(self, X):
        """Return only selected features"""
        if self.selected_indices_ is None:
            raise ValueError("No feature selection was made.")
        return X[:, self.selected_indices_]

    def fit_transform(self, X, y):
        """Performing fit and transform simultaneously"""
        return self.fit(X, y).transform(X)

    def get_feature_distribution(self):
        """Returns the distribution of selected features by groups"""
        if self.selected_indices_ is None:
            raise ValueError("No feature selection was made.")

        distribution = {}

        for group_name, indices in self.group_indices_.items():
            selected_from_group = sum(1 for idx in self.selected_indices_ if idx in indices)
            distribution[group_name] = selected_from_group

        return distribution

