def run_feature_group_ablation_study(X_combined, y, feature_groups, model_dir='gsk3_results/ablation', n_cv_folds=5, use_ensemble=False):
    """
    Ablation studies that compare model performance by excluding one group of features at a time.
    """

    # Create a directory to store results
    os.makedirs(model_dir, exist_ok=True)

    print("Running a feature group ablation study...")
    print(f"Original feature matrix size: {X_combined.shape}")

    # Calculating feature group index ranges
    group_indices = {}
    start_idx = 0
    for group, count in feature_groups.items():
        end_idx = start_idx + count
        group_indices[group] = list(range(start_idx, end_idx))
        start_idx = end_idx

    # Set up an experiment
    experiments = {
        'All Features': None,  # Enable all features (baseline)
    }

    # Add an experiment that excludes each group of features
    for group in feature_groups.keys():
        experiments[f'No {group}'] = group

    # Save the results
    ablation_results = {
        'experiment': [],
        'r2_mean': [],
        'r2_std': [],
        'rmse_mean': [],
        'rmse_std': [],
        'removed_features': []
    }

    # Set up cross-validation
    kf = KFold(n_splits=n_cv_folds, shuffle=True, random_state=42)

    # Run each experiment
    for exp_name, excluded_group in experiments.items():
        print(f"\n실험: {exp_name}")
        start_time = time.time()

        # Feature selection (if there are groups to exclude)
        if excluded_group is not None:
            # Indexes to exclude
            excluded_indices = group_indices[excluded_group]
            # Indexes to include (all indexes except those to exclude)
            included_indices = [i for i in range(X_combined.shape[1]) if i not in excluded_indices]
            # Feature selection
            X_selected = X_combined[:, included_indices]
            removed_features = len(excluded_indices)
            print(f"Excluded features group: {excluded_group} ({removed_features} features)")
        else:
            # Enable all features
            X_selected = X_combined
            removed_features = 0

        print(f"Number of features selected: {X_selected.shape[1]}")

        # Cross-validation performance
        r2_scores = []
        rmse_scores = []

        for fold, (train_idx, test_idx) in enumerate(kf.split(X_selected)):
            X_train, X_test = X_selected[train_idx], X_selected[test_idx]
            y_train, y_test = y[train_idx], y[test_idx]

            # Apply RobustFeaturePipeline (same preprocessing as traditional models)
            feature_pipeline = RobustFeaturePipeline()
            feature_pipeline.original_feature_dim = X_train.shape[1]

            # Remove immutable features
            feature_pipeline.non_constant_mask = np.std(X_train, axis=0) != 0
            X_train_filtered = X_train[:, feature_pipeline.non_constant_mask]
            X_test_filtered = X_test[:, feature_pipeline.non_constant_mask]

            # Scaling
            feature_pipeline.scaler = RobustScaler()
            X_train_scaled = feature_pipeline.scaler.fit_transform(X_train_filtered)
            X_test_scaled = feature_pipeline.scaler.transform(X_test_filtered)

            # Use a single XGBoost model (computational efficiency)
            model = xgb.XGBRegressor(
                n_estimators=200,
                learning_rate=0.02,
                max_depth=5,
                subsample=0.7,
                colsample_bytree=0.7,
                reg_alpha=0.05,
                reg_lambda=1.0,
                random_state=42
            )

            model.fit(X_train_scaled, y_train)
            y_pred = model.predict(X_test_scaled)

            # Evaluate performance
            r2 = r2_score(y_test, y_pred)
            rmse = np.sqrt(mean_squared_error(y_test, y_pred))

            r2_scores.append(r2)
            rmse_scores.append(rmse)

            print(f"  폴드 {fold+1}: R² = {r2:.4f}, RMSE = {rmse:.4f}")

        # Average performance
        r2_mean = np.mean(r2_scores)
        r2_std = np.std(r2_scores)
        rmse_mean = np.mean(rmse_scores)
        rmse_std = np.std(rmse_scores)

        end_time = time.time()
        elapsed_time = end_time - start_time

        print(f"  Average: R² = {r2_mean:.4f} ± {r2_std:.4f}, RMSE = {rmse_mean:.4f} ± {rmse_std:.4f}")
        print(f"  Experiment time: {elapsed_time:.2f}sec ({elapsed_time/60:.2f}min)")

        # Save the results
        ablation_results['experiment'].append(exp_name)
        ablation_results['r2_mean'].append(r2_mean)
        ablation_results['r2_std'].append(r2_std)
        ablation_results['rmse_mean'].append(rmse_mean)
        ablation_results['rmse_std'].append(rmse_std)
        ablation_results['removed_features'].append(removed_features)

    # Visualize results
    plot_ablation_results(ablation_results, model_dir)

    return ablation_results

