def get_essential_2d_descriptors(mol):
    """
    Optimized function to calculate only essential 2D descriptors
    """
    if mol is None:
        return [0] * 50  # Return only core descriptors

    result_descriptors = []
    try:
        # 1. Basic druglikeness properties (10)
        result_descriptors.extend([
            Descriptors.MolWt(mol),                  # Molecular weight
            Descriptors.MolLogP(mol),                # LogP
            Descriptors.NumHDonors(mol),             # H-bond donors count
            Descriptors.NumHAcceptors(mol),          # H-bond acceptors count
            Descriptors.NumRotatableBonds(mol),      # Rotatable bonds count
            Descriptors.RingCount(mol),              # Ring count
            Descriptors.TPSA(mol),                   # Topological polar surface area
            Descriptors.NumAromaticRings(mol),       # Aromatic rings count
            Descriptors.NumAliphaticRings(mol),      # Aliphatic rings count
            Descriptors.FractionCSP3(mol)            # sp3 carbon ratio
        ])

        # 2. Atom count related (10)
        atom_counts = Counter([atom.GetSymbol() for atom in mol.GetAtoms()])
        result_descriptors.extend([
            atom_counts.get('C', 0),        # Carbon count
            atom_counts.get('N', 0),        # Nitrogen count
            atom_counts.get('O', 0),        # Oxygen count
            atom_counts.get('S', 0),        # Sulfur count
            atom_counts.get('F', 0),        # Fluorine count
            atom_counts.get('Cl', 0),       # Chlorine count
            atom_counts.get('Br', 0),       # Bromine count
            mol.GetNumAtoms(),              # Total atoms count
            Descriptors.HeavyAtomCount(mol),# Heavy atoms count
            Descriptors.NumHeteroatoms(mol) # Heteroatoms count
        ])

        # 3. Bond/ring related properties (10)
        result_descriptors.extend([
            rdMolDescriptors.CalcNumRings(mol),                  # Total rings count
            rdMolDescriptors.CalcNumAromaticRings(mol),          # Aromatic rings count
            rdMolDescriptors.CalcNumAliphaticRings(mol),         # Aliphatic rings count
            rdMolDescriptors.CalcNumSaturatedRings(mol),         # Saturated rings count
            rdMolDescriptors.CalcNumAliphaticHeterocycles(mol),  # Aliphatic heterocycles count
            rdMolDescriptors.CalcNumAromaticHeterocycles(mol),   # Aromatic heterocycles count
            rdMolDescriptors.CalcNumSaturatedHeterocycles(mol),  # Saturated heterocycles count
            rdMolDescriptors.CalcNumSpiroAtoms(mol),             # Spiro atoms count
            rdMolDescriptors.CalcNumBridgeheadAtoms(mol),        # Bridgehead atoms count
            Descriptors.qed(mol)                                # QED (druglikeness)
        ])

        # 4. Complexity and connectivity indices (10)
        result_descriptors.extend([
            Descriptors.BalabanJ(mol),      # Balaban J index
            Descriptors.BertzCT(mol),       # Bertz CT (complexity)
            Descriptors.HallKierAlpha(mol), # Hall-Kier alpha
            Descriptors.Kappa1(mol),        # Kappa 1
            Descriptors.Kappa2(mol),        # Kappa 2
            Descriptors.Chi0(mol),          # Chi 0
            Descriptors.Chi1(mol),          # Chi 1
            Descriptors.Chi2n(mol),         # Chi 2n
            Descriptors.Chi3n(mol),         # Chi 3n
            Descriptors.Chi4n(mol)          # Chi 4n
        ])

        # 5. Specific ratio related properties (10)
        num_atoms = mol.GetNumAtoms()
        num_bonds = mol.GetNumBonds()

        # Atom/bond ratios
        if num_atoms > 0:
            result_descriptors.append(num_bonds / num_atoms)  # Bonds/atoms ratio

            # Aromatic atoms ratio
            aromatic_atoms = sum(1 for atom in mol.GetAtoms() if atom.GetIsAromatic())
            result_descriptors.append(aromatic_atoms / num_atoms)

            # Nitrogen/oxygen ratio
            result_descriptors.append((atom_counts.get('N', 0) + atom_counts.get('O', 0)) / num_atoms)

            # Heteroatoms ratio
            result_descriptors.append(Descriptors.NumHeteroatoms(mol) / num_atoms)

            # sp3 carbon ratio
            result_descriptors.append(Descriptors.FractionCSP3(mol))
        else:
            result_descriptors.extend([0, 0, 0, 0, 0])

        # Bond type ratios
        if num_bonds > 0:
            bond_types = {
                Chem.BondType.SINGLE: 0,
                Chem.BondType.DOUBLE: 0,
                Chem.BondType.TRIPLE: 0,
                Chem.BondType.AROMATIC: 0
            }

            for bond in mol.GetBonds():
                bond_type = bond.GetBondType()
                if bond_type in bond_types:
                    bond_types[bond_type] += 1

            result_descriptors.extend([
                bond_types[Chem.BondType.SINGLE] / num_bonds,
                bond_types[Chem.BondType.DOUBLE] / num_bonds,
                bond_types[Chem.BondType.TRIPLE] / num_bonds,
                bond_types[Chem.BondType.AROMATIC] / num_bonds,
                Descriptors.NumRotatableBonds(mol) / num_bonds  # Rotatable bonds ratio
            ])
        else:
            result_descriptors.extend([0, 0, 0, 0, 0])

    except Exception as e:
        print(f"Error calculating 2D descriptors: {e}")
        return [0] * 50

    # Replace missing values with 0
    result_descriptors = [0 if x is None or np.isnan(x) or np.isinf(x) else x for x in result_descriptors]

    return result_descriptors[:50]  # Return 50 core descriptors

def generate_ecfp_fingerprint(mol, radius=3, nBits=1024, useFeatures=True):
    """
    Optimized ECFP fingerprint calculation function
    """
    if mol is None:
        return np.zeros(nBits, dtype=np.int32)

    try:
        # Calculate ECFP fingerprint
        fp = AllChem.GetMorganFingerprintAsBitVect(
            mol,
            radius,
            nBits=nBits,
            useFeatures=useFeatures
        )
        return np.array(fp)
    except Exception as e:
        print(f"Error generating ECFP: {e}")
        return np.zeros(nBits, dtype=np.int32)

