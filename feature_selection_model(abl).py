def get_optimized_3d_descriptors(mol_3d):
    """
    Calculate only 3D descriptors important for GSK3-beta inhibitors
    """
    descriptors_3d = []

    if mol_3d is not None and mol_3d.GetNumConformers() > 0:
        try:
            # 1. Molecular shape descriptors (ATP binding pocket shape fit)
            descriptors_3d.extend([
                Descriptors3D.Asphericity(mol_3d),            # Asphericity
                Descriptors3D.Eccentricity(mol_3d),           # Eccentricity
                Descriptors3D.InertialShapeFactor(mol_3d),    # Inertial shape factor
                Descriptors3D.NPR1(mol_3d),                   # Normalized principal moment ratio 1
                Descriptors3D.NPR2(mol_3d),                   # Normalized principal moment ratio 2
                Descriptors3D.RadiusOfGyration(mol_3d),       # Radius of gyration
                Descriptors3D.SpherocityIndex(mol_3d)         # Spherocity index
            ])

            # 2. Molecular size and volume (GSK3-beta binding site size related)
            try:
                # Molecular surface area
                mol_area = AllChem.ComputeMolSurfaceArea(mol_3d)
                # Molecular volume
                volume = Descriptors3D.GetMolecularVolume(mol_3d)
                # Surface area to volume ratio
                sa_vol_ratio = mol_area / max(0.001, volume)

                descriptors_3d.extend([
                    mol_area,       # Molecular surface area
                    volume,         # Molecular volume
                    sa_vol_ratio    # Surface area to volume ratio
                ])
            except:
                descriptors_3d.extend([0, 0, 0])

            # 3. Interatomic distance statistics (binding pocket interaction distances)
            conf = mol_3d.GetConformer()
            positions = [conf.GetAtomPosition(i) for i in range(mol_3d.GetNumAtoms())]

            if len(positions) > 1:
                # Calculate distances between all atom pairs
                distances = []
                for i in range(len(positions)):
                    for j in range(i+1, len(positions)):
                        distances.append(positions[i].Distance(positions[j]))

                # Distance statistics
                descriptors_3d.extend([
                    np.max(distances),      # Maximum interatomic distance
                    np.min(distances),      # Minimum interatomic distance
                    np.mean(distances),     # Average interatomic distance
                    np.std(distances)       # Standard deviation of interatomic distances
                ])
            else:
                descriptors_3d.extend([0, 0, 0, 0])

            # 4. GSK3-beta specific 3D properties
            # Kinase binding pocket related property calculations
            try:
                # Identify hydrogen bonding capable atoms and their positions
                h_bond_atoms = []
                atoms = mol_3d.GetAtoms()

                for atom in atoms:
                    symbol = atom.GetSymbol()
                    idx = atom.GetIdx()

                    # Oxygen or nitrogen (potential H-bond acceptors)
                    if symbol in ['O', 'N']:
                        pos = conf.GetAtomPosition(idx)
                        h_bond_atoms.append((pos, 'acceptor', idx))

                    # Potential H-bond donors (NH, OH groups)
                    if symbol in ['N', 'O'] and atom.GetTotalNumHs() > 0:
                        pos = conf.GetAtomPosition(idx)
                        h_bond_atoms.append((pos, 'donor', idx))

                # Analyze distances between H-bond sites
                hbond_distances = []
                for i, (pos1, type1, idx1) in enumerate(h_bond_atoms):
                    for j, (pos2, type2, idx2) in enumerate(h_bond_atoms):
                        if i < j and type1 != type2:  # Find acceptor-donor pairs
                            dist = pos1.Distance(pos2)
                            hbond_distances.append(dist)

                # H-bond distance statistics
                if hbond_distances:
                    descriptors_3d.extend([
                        min(hbond_distances),              # Minimum H-bond distance
                        np.mean(hbond_distances),          # Average H-bond distance
                        len(hbond_distances)               # Number of potential H-bond pairs
                    ])
                else:
                    descriptors_3d.extend([0, 0, 0])

                # 5. Aromatic ring plane orientations (ATP binding pocket orientation)
                # Find aromatic rings
                aromatic_rings = []
                ring_info = mol_3d.GetSSSR()

                for ring in ring_info:
                    if all(mol_3d.GetAtomWithIdx(idx).GetIsAromatic() for idx in ring):
                        ring_atoms = [conf.GetAtomPosition(idx) for idx in ring]
                        if len(ring_atoms) >= 3:
                            aromatic_rings.append(ring_atoms)

                # Calculate ring planes
                ring_normals = []
                for ring_atoms in aromatic_rings:
                    if len(ring_atoms) >= 3:
                        try:
                            # Calculate normal vector from first three atoms of plane
                            v1 = ring_atoms[1].DirectionVector(ring_atoms[0])
                            v2 = ring_atoms[2].DirectionVector(ring_atoms[0])
                            normal = [
                                v1.y * v2.z - v1.z * v2.y,
                                v1.z * v2.x - v1.x * v2.z,
                                v1.x * v2.y - v1.y * v2.x
                            ]
                            # Normalize vector
                            norm = np.sqrt(sum(n*n for n in normal))
                            if norm > 0:
                                normal = [n/norm for n in normal]
                                ring_normals.append(normal)
                        except:
                            continue

                # Analyze angles between ring planes
                ring_angles = []
                for i, n1 in enumerate(ring_normals):
                    for j, n2 in enumerate(ring_normals):
                        if i < j:
                            try:
                                # Dot product between normal vectors
                                dot_product = sum(a*b for a, b in zip(n1, n2))
                                # Adjust for numerical errors in dot product
                                dot_product = max(-1, min(1, dot_product))
                                angle = np.arccos(dot_product) * 180 / np.pi
                                # Find angle closest to perpendicular (90 degrees is important)
                                angle = min(angle, 180 - angle)
                                ring_angles.append(angle)
                            except:
                                continue

                # Aromatic ring orientation statistics
                if ring_angles:
                    descriptors_3d.extend([
                        np.mean(ring_angles),        # Average inter-ring angle
                        len(aromatic_rings),         # Number of aromatic rings
                        len(ring_angles)             # Number of inter-ring angle pairs
                    ])
                else:
                    descriptors_3d.extend([0, 0, 0])
            except:
                descriptors_3d.extend([0, 0, 0, 0, 0, 0])

        except Exception as e:
            print(f"Error calculating 3D descriptors: {e}")
            descriptors_3d = [0] * 20  # Default values
    else:
        descriptors_3d = [0] * 20  # No 3D structure case

    # Fix length to 20
    if len(descriptors_3d) < 20:
        descriptors_3d.extend([0] * (20 - len(descriptors_3d)))

    return descriptors_3d[:20]  # Return only 20 core 3D descriptors

def get_gsk3beta_specific_descriptors(mol):
    """
    Target-specific descriptors specialized for GSK3-beta inhibitors
    """
    if mol is None:
        return [0] * 30  # Number of target-specific descriptors

    specific_descriptors = []
    try:
        # 1. Hydrogen bonding properties (related to Asp133, Val135, Arg141 residues in GSK3-beta)
        hbond_donors = Lipinski.NumHDonors(mol)
        hbond_acceptors = Lipinski.NumHAcceptors(mol)
        heavy_atoms = Descriptors.HeavyAtomCount(mol)

        # Atom count calculations
        atom_symbols = [atom.GetSymbol() for atom in mol.GetAtoms()]
        n_count = atom_symbols.count('N')
        o_count = atom_symbols.count('O')
        s_count = atom_symbols.count('S')

        # Prevent division by zero in ratio calculations
        heavy_atoms_safe = max(1, heavy_atoms)

        specific_descriptors.extend([
            hbond_donors,                                       # H-bond donors count
            hbond_acceptors,                                    # H-bond acceptors count
            hbond_donors / heavy_atoms_safe,                    # H-bond donors per heavy atom
            hbond_acceptors / heavy_atoms_safe,                 # H-bond acceptors per heavy atom
            (hbond_donors + hbond_acceptors) / heavy_atoms_safe # Total H-bonding potential
        ])

        # 2. Aromatic properties (related to hydrophobic pocket in GSK3-beta)
        aromatic_rings = Descriptors.NumAromaticRings(mol)
        total_rings = rdMolDescriptors.CalcNumRings(mol)
        total_rings_safe = max(1, total_rings)

        specific_descriptors.extend([
            aromatic_rings,                                      # Aromatic rings count
            aromatic_rings / total_rings_safe,                   # Aromatic/total rings ratio
            rdMolDescriptors.CalcNumAromaticCarbocycles(mol),    # Aromatic carbocycles count
            rdMolDescriptors.CalcNumAromaticHeterocycles(mol)    # Aromatic heterocycles count
        ])

        # 3. Atom type properties (GSK3-beta selectivity related)
        specific_descriptors.extend([
            n_count,                       # Nitrogen count
            o_count,                       # Oxygen count
            s_count,                       # Sulfur count
            n_count / heavy_atoms_safe,    # Nitrogen ratio
            o_count / heavy_atoms_safe,    # Oxygen ratio
            s_count / heavy_atoms_safe     # Sulfur ratio
        ])

        # 4. Size and flexibility properties (ATP binding pocket related)
        rotatable_bonds = Descriptors.NumRotatableBonds(mol)
        num_bonds = mol.GetNumBonds()
        num_bonds_safe = max(1, num_bonds)

        mw = Descriptors.MolWt(mol)
        logp = Descriptors.MolLogP(mol)

        specific_descriptors.extend([
            rotatable_bonds,                       # Rotatable bonds count
            rotatable_bonds / num_bonds_safe,      # Rotatable bonds ratio
            mw,                                    # Molecular weight
            logp,                                  # LogP
            Descriptors.TPSA(mol)                  # Topological polar surface area
        ])

        # 5. GSK3-beta specific composite indices (ATP binding pocket interactions)
        # Hinge region interaction score
        hinge_score = (aromatic_rings * 0.5 +
                      hbond_donors * 0.3 +
                      hbond_acceptors * 0.2)

        # Hydrophobic pocket interaction score
        hydrophobic_score = (logp * 0.6 +
                            aromatic_rings * 0.4)

        # Active site shape complementarity (size fitness)
        # GSK3-beta prefers medium-sized molecules (around 300-500Da)
        size_match = 1.0 - min(abs(mw - 400) / 400, 1.0)

        # Flexibility penalty (lower flexibility is better for binding)
        flexibility_penalty = min(rotatable_bonds / 10.0, 1.0)

        # GSK3-beta vs GSK3-alpha selectivity score
        # Estimated by nitrogen content and aromatic properties
        selectivity_score = (n_count * 0.3 +
                           aromatic_rings * 0.4 +
                           (hbond_donors + hbond_acceptors) * 0.3) / 10.0

        specific_descriptors.extend([
            hinge_score,               # Hinge region interaction
            hydrophobic_score,         # Hydrophobic pocket interaction
            size_match,                # Size fitness
            1.0 - flexibility_penalty, # Flexibility optimization (lower is better)
            selectivity_score          # GSK3-beta selectivity
        ])

        # 6. Charge-related properties (interactions with charged residues in GSK3-beta)
        formal_charges = [atom.GetFormalCharge() for atom in mol.GetAtoms()]
        pos_charges = sum(1 for c in formal_charges if c > 0)
        neg_charges = sum(1 for c in formal_charges if c < 0)

        # Cationic nitrogen count (NH, NH2, NH3+)
        n_atoms = [atom for atom in mol.GetAtoms() if atom.GetSymbol() == 'N']
        cationic_n = sum(1 for atom in n_atoms if atom.GetTotalNumHs() > 0)

        specific_descriptors.extend([
            pos_charges,                   # Positive charges count
            neg_charges,                   # Negative charges count
            cationic_n,                    # Cationic nitrogen count
            pos_charges / heavy_atoms_safe, # Positive charge ratio
            n_count * hbond_donors / (heavy_atoms_safe * 10)  # N-H bonding potential
        ])

    except Exception as e:
        print(f"Error calculating GSK3-beta specific descriptors: {e}")
        return [0] * 30

    # Replace missing or infinite values with 0
    specific_descriptors = [0 if x is None or np.isnan(x) or np.isinf(x) else x for x in specific_descriptors]

    return specific_descriptors[:30]  # Return 30 target-specific descriptors

